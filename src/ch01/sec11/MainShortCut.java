package ch01.sec11;

public class MainShortCut {
	public static void main(String[] args) { //main + Ctrl + spaceBar
		System.out.println("Hi JAVA"); //sysout + Ctrl + spaceBar
		System.out.println("출력되는 내용");
		System.out.println("출력!");
		
		// 주석 : Ctrl + /
		// 실행 : Ctrl + F11
		// 창크기 : Ctrl + m
		// 한줄지우기 : Ctrl + d
		// 줄 옮기기 : Alt + 위아래 방향키
		// 힌트 얻기 : Ctrl + spaceBar
		
		
	}

}
